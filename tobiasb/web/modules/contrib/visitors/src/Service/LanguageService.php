  /**
   * {@inheritdoc}
   */
  public function getLanguageLabel($language_code): MarkupInterface {
    $code = strtolower(trim((string) $language_code));
    return isset(self::LANGUAGES[$code])
      ? new FormattableMarkup('@language', ['@language' => self::LANGUAGES[$code]])
      : $this->t('Unknown');
  }

